public class basic{
    public static void main(String args[]){
        // operators in java
        // 1. Arithmetic operator
            // Binary operators
            int a = 10;
            int b = 20;
            System.out.println(a+b);
            System.out.println(a-b);
            System.out.println(a*b);
            System.out.println(a/b);
            System.out.println(a%b);
            // uninary operators
            a++;
            System.out.println(a);
    }
}